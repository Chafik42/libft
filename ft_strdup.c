#include "libft.h"

char	*ft_strdup(const char *s)
{
	int			i;
	char		*str;
	char		*dest;

	i = 0;
	str = (char *)s;
	dest = (char *) malloc(sizeof(char) * (ft_strlen(str) + 1));
	if (!dest)
		return (NULL);
	while (str[i] != '\0')
	{
		dest[i] = str[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
